package edu.java.intro2;

public class AppMain2 {
	public static void main(String[] args) {
		System.out.println("자바 프로그램 실행 방법");
		System.out.println("Menu-> Run-> Run As");
		System.out.println("Ctrl+F11");
		// 문자열(String) : 문자들의 집합(sequence). 큰따옴표("")를 사용
		
		//Ctrl+space : 코드 추천 및 자동 완성.
		System.out.println("문자열"+"합치기"); // + : 문자열을 이어주는 연산자
		System.out.println(1+2); // + : 덧셈 연산자
		System.out.println("1"+"2"); 
		System.out.println("1 + 2 = 3"); //1 + 2 = 3 출력
		System.out.println("1+2="+(1+2)); //1+2=3 
		
		System.out.print("프린트");
		System.out.print("프린트"); //프린트프린트 출력
	}

}
